#include "libft.h"

void		print_bits(unsigned char octet)
{
	int		i;

	i = 8;
	while (i > 0)
	{
		(octet & 128) ? write(1, "1", 1) : write(1, "0", 1);
		octet <<= 1;
		i--;
	}
}
