#include "libft.h"

uint64_t		ft_swap_endian64(uint64_t n)
{
	return (((n << 56) | \
			((n & 0x000000000000FF00) << 40) | \
			((n & 0x0000000000FF0000) << 24) | \
			((n & 0x00000000FF000000) << 8) | \
			((n & 0x000000FF00000000) >> 8) | \
			((n & 0x0000FF0000000000) >> 24) | \
			((n & 0x00FF000000000000) >> 40) | \
			(n >> 56)));
}
